import numpy as np

# dois vetores a e b com 3 elementos cada
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# calcule: a + b, a - b e 2 * a
# imprima os resultados
soma = a + b
subtracao = a - b
multiplicacao_por_escalar = 2 * a

print('Soma:', soma)
print('Subtração:', subtracao)
print('Multiplicação por escalar:', multiplicacao_por_escalar)


