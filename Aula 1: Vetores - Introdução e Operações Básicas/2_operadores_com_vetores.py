import numpy as np

# Vetores
u = np.array([1, 2, 3])
v = np.array([4, -1, 2])

# Adição
soma = u + v
print("Soma:", soma)

# Subtração
subtracao = u - v
print("Subtração:", subtracao)

# Multiplicação por escalar
escalar = 2
multiplicacao = escalar * v
print("Multiplicação por escalar:", multiplicacao)
